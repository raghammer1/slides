// Global variable for the width of a container.
export let containerWidth = 1000;

// Sets a new width for the container.
export const setContainerWidth = (newWidth) => {
  containerWidth = newWidth;
};

// Global variable for the height of a container.
export let containerHeight = 500;

// Sets a new height for the container.
export const setContainerHeight = (newWidth) => {
  containerHeight = newWidth;
};
